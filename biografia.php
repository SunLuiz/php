    <p>Elvis Aaron Presley nasceu em Vernon e Gladys Presley em uma casa de dois quartos em Tupelo, Mississippi, em 8 de janeiro de 1935. Seu irmão gêmeo, Jessie Garon, nasceu morto, deixando Elvis crescer como filho único.

Elvis e seus pais mudaram-se para Memphis, Tennessee, em 1948, e Elvis se formou na Humes High School em 1953. As influências musicais de Elvis foram a música pop e country da época, a música gospel que ele ouvia na igreja e no mundo todo. o gospel noturno canta com frequência, e o R & B preto que ele absorveu na histórica rua Beale como um adolescente de Memphis. Em 1954, Elvis começou sua carreira de cantor com a lendária gravadora Sun Records em Memphis. No final de 1955, seu contrato de gravação foi vendido para a RCA Victor. Em 1956, ele era uma sensação internacional.

Com um som e estilo que unicamente combinavam as diversas influências musicais de Elvis e confundiam e desafiavam as barreiras sociais e raciais da época, ele inaugurou uma nova era da música americana e da cultura popular. A dinâmica história de vida de Elvis Presley, desde o seu início humilde até à fama, é uma viagem fascinante que valeu a Elvis o seu título ainda invicto de ' King of Rock' N Roll '. </p>

    
